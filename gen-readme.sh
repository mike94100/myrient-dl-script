                    size_num=$(echo "$size_info" | sed 's/^\([0-9.]*\).*/\1/')
                    size_unit=$(echo "$size_info" | sed 's/^[0-9.]*\s*//' | sed 's/\s*$//')
                    file_bytes=$(format_file_size "$size_num" "$size_unit" "B")
                    platform_size_bytes=$((platform_size_bytes + file_bytes))
                fi
            done

