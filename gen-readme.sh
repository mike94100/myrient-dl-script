# Parse TOML using Python
parse_toml() {
python3 - "$1" <<'EOF'
import sys, json

try:
    import tomllib  # Python 3.11+
except ImportError:
    import tomli as tomllib  # fallback for older Python

with open(sys.argv[1], "rb") as f:
    data = tomllib.load(f)

print(json.dumps(data))
EOF
}

# Convert file size from one unit to another
# Usage: format_file_size <size> <from_unit> <to_unit>
format_file_size() {
    local size="$1"
    local from_unit="$2"
    local to_unit="$3"

    # Define unit indices (powers of 1024 from B)
    declare -A unit_indices=(
        ["B"]=0
        ["KiB"]=1
        ["MiB"]=2
        ["GiB"]=3
        ["TiB"]=4
        ["PiB"]=5
    )

    # Get unit indices
    local from_index="${unit_indices[$from_unit]}"
    local to_index="${unit_indices[$to_unit]}"

    # If units are not recognized, log error and return 0
    if [[ -z "$from_index" || -z "$to_index" ]]; then
        log "ERROR: Unknown size units - from: '$from_unit' (index: $from_index), to: '$to_unit' (index: $to_index)"
        echo "0"
        return
    fi

    # Calculate number of 1024 multiplications/divisions needed
    local num_repeats=$((to_index - from_index))

    # Perform the conversion using awk for floating point arithmetic
    local result
    if [[ $num_repeats -gt 0 ]]; then
        # Converting to larger unit (e.g., B to KiB), divide by 1024^num_repeats
        result=$(awk "BEGIN {printf \"%.0f\", $size / (1024 ^ $num_repeats)}")
    elif [[ $num_repeats -lt 0 ]]; then
        # Converting to smaller unit (e.g., KiB to B), multiply by 1024^|num_repeats|
        num_repeats=$(( -num_repeats ))
        result=$(awk "BEGIN {printf \"%.0f\", $size * (1024 ^ $num_repeats)}")
    else
        # Same unit, no conversion needed
        result=$(awk "BEGIN {printf \"%.0f\", $size}")
    fi

    echo "$result"
}

# Check if a TOML is a meta config (contains platform_tomls[] array)
is_meta_toml() {
    local file="$1"
    local json=$(parse_toml "$file")
    local platform_tomls=$(printf "%s" "$json" | jq -r '.platform_tomls // empty')
    [[ -n "$platform_tomls" ]]
}

log() {
    echo "$*" >&2
}

show_help() {
    cat << EOF
NAME
    gen-readme.sh - Generate README.md files from platform TOML configurations

SYNOPSIS
    gen-readme.sh [OPTIONS] <toml_file>

DESCRIPTION
    This script reads a platform TOML file (generated by gen-platform-toml.sh) and
    creates a human-readable README.md file with decoded filenames and metadata.

ARGUMENTS
    toml_file
        Path to the platform TOML file (e.g., gb.toml)

OPTIONS
    -h, --help
        Display this help message and exit.

OUTPUT
    Creates a README.md file in the same directory as the input TOML file.
    The README contains:
    - Platform title and description
    - Generation date and time (UTC)
    - Source URL
    - Total number of files
    - Total download size (calculated from source)
    - Complete list of human-readable filenames

EXAMPLES
    gen-readme.sh testconfigs/gb.toml
    gen-readme.sh --help

SEE ALSO
    gen-platform-toml.sh - Generate TOML files by scraping Myrient pages
    myrient-dl.sh - Download ROMs using TOML configurations

EOF
}

