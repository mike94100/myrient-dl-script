            kb_size=$(awk "BEGIN {kb = $TOTAL_SIZE_BYTES / 1000; printf kb >= 100 ? \"%.0f\" : \"%.1f\", kb}")
            kib_size=$(awk "BEGIN {kib = $TOTAL_SIZE_BYTES / 1024; printf kib >= 100 ? \"%.0f\" : \"%.1f\", kib}")
            TOTAL_SIZE_FORMATTED="${kb_size} KB (${kib_size} KiB)"
